/**A Função eval() em JavaScript: Uma Ferramenta Poderosa e Perigosa
A função eval() em JavaScript é uma ferramenta que permite executar código JavaScript representado como uma
 string. Em outras palavras, você pode passar uma string contendo qualquer código JavaScript válido para a 
 função eval(), e ela irá executar esse código como se estivesse escrito diretamente no seu script.

Exemplo: */

let resultado = eval("2 + 2");
console.log(resultado); // Output: 4


